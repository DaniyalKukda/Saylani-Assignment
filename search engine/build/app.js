                + "</td>"
                + "<td>" +
                mobilePhones[key][key2][key3].Size
                + "</td>"
                + "<td>" +
                mobilePhones[key][key2][key3].Built_in
                + "</td>"
                + "<td>" +
                mobilePhones[key][key2][key3].Camera
                + "</td>"
                + "<td>" +
                mobilePhones[key][key2][key3].Colors
                + "</td>"+ "<td>" +
                mobilePhones[key][key2][key3].Price_in_Rs
                + "</td>"
                + "</tr>";
